// Parametros y Argumentos en Funciones
// a y b son parametros de funcion
function sumar(a, b) {
  console.log(a + b);
}

sumar(2, 3); // 2 y 3 son argumentos
sumar(42, 58);

function saludar(nombre, apellido) {
  console.log(`Hola ${nombre} ${apellido}`);
}

saludar("David", "Rodriguez");
saludar();
